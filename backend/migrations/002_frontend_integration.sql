ALTER TABLE APP_USERS MODIFY (LOGIN_ID NULL, EMAIL NULL);
ALTER TABLE APP_USERS ADD (
    KAKAO_ID VARCHAR2(100),
    DISTRICT_NAME VARCHAR2(30)
);
CREATE UNIQUE INDEX UQ_APP_USERS_KAKAO_ID ON APP_USERS(KAKAO_ID);

CREATE TABLE DISTRICTS (
    NAME VARCHAR2(30) PRIMARY KEY
);

CREATE TABLE MISSIONS (
    MISSION_ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TITLE VARCHAR2(255) NOT NULL,
    DISTRICT_NAME VARCHAR2(30) NOT NULL REFERENCES DISTRICTS(NAME),
    LOCATION VARCHAR2(255) NOT NULL,
    LATITUDE BINARY_DOUBLE,
    LONGITUDE BINARY_DOUBLE,
    RADIUS_M NUMBER DEFAULT 300 NOT NULL,
    REWARD_POINTS NUMBER DEFAULT 0 NOT NULL,
    MISSION_TYPE VARCHAR2(30) NOT NULL,
    IMAGE_URL VARCHAR2(1000),
    CONSTRAINT CK_MISSIONS_TYPE
        CHECK (MISSION_TYPE IN ('PHOTO', 'CURRENT_LOCATION', 'RECEIPT'))
);

CREATE TABLE USER_MISSIONS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    USER_CODE VARCHAR2(20) NOT NULL REFERENCES APP_USERS(USER_CODE),
    MISSION_ID NUMBER NOT NULL REFERENCES MISSIONS(MISSION_ID),
    STATUS VARCHAR2(30) DEFAULT 'completed' NOT NULL,
    VERIFIED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    PHOTO_URL VARCHAR2(1000),
    RECEIPT_IMAGE_URL VARCHAR2(1000),
    CONSTRAINT UQ_USER_MISSION UNIQUE (USER_CODE, MISSION_ID)
);

